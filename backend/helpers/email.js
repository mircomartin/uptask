import nodemailer from "nodemailer";

export const emailRegistro = async (datos) => {
  const { email, nombre, token } = datos;

  const transport = nodemailer.createTransport({
    host: process.env.EMAIL_HOST,
    port: process.env.EMAIL_PORT,
    auth: {
      user: process.env.EMAIL_USER,
      pass: process.env.EMAIL_PASS,
    },
  });

  // Información del email

  const info = await transport.sendMail({
    from: '"UpTask - Customize your Projects" <cuentas@uptask.com>',
    to: email,
    subject: "UpTask - Confirm your account",
    text: "Confirm your account",
    html: `<p>Hi!: ${nombre} Confirm your account in UpTask</p>
    <p>Your account is almost ready, you just must confirm in the next link: 

    <a href="${process.env.FRONTEND_URL}/auth/confirm-account/${token}">Confirm</a>
    
    <p>If you dont create this account, you must ignore this email.</p>
    
    
    `,
  });
};

